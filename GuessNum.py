from random import randrange;

print(" *** Welcome to random number guessing game *** ")
r=randrange(1,20)
n=''
while r!= n:
    n=int(input("enter any num: "))
    if(n==r):
        print("Yayieee you guessed it correctly")
        break
    
    elif n<r:
        print("Try a higher number!!")
        continue
    elif(n>r):
        print("Try a smaller number!!")
        continue
